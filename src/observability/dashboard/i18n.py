def init_language() -> None:
    """Initialize language selection in session state."""
    if "language" not in st.session_state:
        st.session_state["language"] = "zh"
    # Keep lang_selector in sync with language on first load
    if "lang_selector" not in st.session_state:
        st.session_state["lang_selector"] = st.session_state["language"]


def _on_lang_change() -> None:
    st.session_state["language"] = st.session_state["lang_selector"]


def render_language_selector() -> None:
    """Render a compact language toggle in the sidebar."""
    init_language()
    st.sidebar.radio(
        t("lang.label"),
        options=["en", "zh"],
        format_func=lambda x: "English" if x == "en" else "中文",
        index=1 if st.session_state["language"] == "zh" else 0,
        horizontal=True,
        key="lang_selector",
        on_change=_on_lang_change,
    )


def t(key: str, *args: object) -> str:
    """Translate a key to the current language.

    Falls back to English if the key is missing in the current language.
    Supports positional format args: t("key.with.{}", value)
    """
    lang = st.session_state.get("language", "zh")
    text = TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS["en"].get(key, key)
    if args:
        return text.format(*args)
    return text
